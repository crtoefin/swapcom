#include "libft.h"

void	*ft_memset(void *b, int c, size_t n)
{
	char	*t;

	if (n > 0)
	{
		t = b;
		while (n)
		{
			*t++ = (unsigned char)c;
			n--;
		}
	}
	return (b);
}
