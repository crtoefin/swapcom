#include "libft.h"

int	ft_atoi(const char *str)
{
	int	s;
	int	nbr;

	nbr = 0;
	s = 1;
	while (*str == ' ' || *str == '\n' || *str == '\r' || *str == '\t'
		|| *str == '\v' || *str == '\f')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -1;
		str++;
	}
	while (*str >= '0' && *str <= '9')
	{
		nbr = (nbr * 10) + (*str - '0');
		str++;
	}
	return (nbr * s);
}
